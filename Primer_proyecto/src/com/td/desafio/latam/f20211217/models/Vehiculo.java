package com.td.desafio.latam.f20211217.models;

public class Vehiculo {
	//atributos
	public String color;
	public Float velocidad;
	public int ruedas;
	public Float motor;
	
	
	public Vehiculo() {
		super();
	}
	
	

	public Vehiculo(String color, Float velocidad, int ruedas, Float motor) {
		super();
		this.color = color;
		this.velocidad = velocidad;
		this.ruedas = ruedas;
		this.motor = motor;
	
}
}
